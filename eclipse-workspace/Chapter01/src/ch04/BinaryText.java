package ch04;

public class BinaryText {

	public static void main(String[] args) {
		int num = 10; // 10진수
		int bNum = 0B1010; //2진수 *
		int oNum = 012;	// 8진수
		int xNum = 0XA; // 16진수
		
		System.out.println(num); 
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(xNum);
	}

}
