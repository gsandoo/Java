package ch08;

public class CharacterTest {

	public static void main(String[] args) {
		
		
		char ch1 = 'A'; // 2바이트
		System.out.println(ch1);//A
		System.out.println((int)ch1);//65
		
		int ch2 =67;
		System.out.println(ch2);//67
		System.out.println((char)ch2);//C
		
		
		//char 은 음수지정 불가능. 2바이트 이상 불가능.
		
		
	}

}
